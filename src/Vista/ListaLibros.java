    public void Rellenado(ArrayList<Libro> Estudiosos) {
        DefaultTableModel dtf = new DefaultTableModel();
        dtf.addColumn("ISBN");
        dtf.addColumn("Titulo");
        dtf.addColumn("Autor");
        dtf.addColumn("Editorial");
        dtf.addColumn("Anho");
        dtf.addColumn("Total");
        dtf.addColumn("Existencia");
        for (int i = 0; i < Estudiosos.size(); i++) {
            String renglon[] = new String[7];
            System.out.println("Nombres: " + Estudiosos.get(i).getTitulo());
            renglon[0] = Estudiosos.get(i).getIsbn();
            renglon[1] = Estudiosos.get(i).getTitulo();
            renglon[2] = Estudiosos.get(i).getAutor();
            renglon[3] = Estudiosos.get(i).getEditorial();
            renglon[4] = Estudiosos.get(i).getAnho();
            renglon[5] = String.valueOf(Estudiosos.get(i).getTotal());
            renglon[6] = String.valueOf(Estudiosos.get(i).getExistencia());
            
            dtf.addRow(renglon);

        }
        jtLibros.setModel(dtf);
    }

