public  void Rellenado(ArrayList<Estudiante> Estudiosos) {
        DefaultTableModel dtf = new DefaultTableModel();
        dtf.addColumn("Numero de control");
        dtf.addColumn("Nombre");
        dtf.addColumn("Apellido P");
        dtf.addColumn("Apellido M");
        dtf.addColumn("Telefono");
        dtf.addColumn("Correo");
        dtf.addColumn("Carrera");
        dtf.addColumn("Permiso");
        for (int i = 0; i < Estudiosos.size(); i++) {
            String renglon[] = new String[8];
            System.out.println("Nombres: " + Estudiosos.get(i).getNomEs());
            renglon[0] = Estudiosos.get(i).getNc();
            renglon[1] = Estudiosos.get(i).getNomEs();
            renglon[2] = Estudiosos.get(i).getApp();
            renglon[3] = Estudiosos.get(i).getApm();
            renglon[4] = Estudiosos.get(i).getTel();
            renglon[5] = Estudiosos.get(i).getCarrera();
            renglon[6] = Estudiosos.get(i).getCorreo();
            if (Estudiosos.get(i).isPermiso()) {
                renglon[7] = "Disponible";
            } else {
                renglon[7] = "No disponible";
            }
            dtf.addRow(renglon);
           
        }
        jtEstudiante.setModel(dtf);
    }

